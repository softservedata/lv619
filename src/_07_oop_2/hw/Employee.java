package _07_oop_2.hw;

abstract class Employee implements Payment{
	////////////////////////////////////////////////////////////////////////////
	String name;
	String employeeId;
	////////////////////////////////////////////////////////////////////////////
	protected Employee(String employeeId, String name) {
		this.name = name;
		this.employeeId = employeeId;
	}
	////////////////////////////////////////////////////////////////////////////
	protected String getName() {return name;}
	protected String getEmployeeId() {return employeeId;}
	////////////////////////////////////////////////////////////////////////////
	@Override
	public abstract double calculatePay();
	////////////////////////////////////////////////////////////////////////////
	@Override
	public String toString() {
		return "name: " + getName() + " ID: " + getEmployeeId() + 
				" average monthly salary: " + 
				Math.round(calculatePay() * 100) / 100d; 
	}
	////////////////////////////////////////////////////////////////////////////
}