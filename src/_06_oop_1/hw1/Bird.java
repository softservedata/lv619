package _06_oop_1.hw1;

abstract class Bird {
	////////////////////////////////////////////////////////////////////////////
	private int feathers;
	private int layEggs;
	////////////////////////////////////////////////////////////////////////////
	protected Bird(int feathers, int layEggs) {
		this.feathers= feathers;
		this.layEggs = layEggs;
	}
	////////////////////////////////////////////////////////////////////////////
	abstract protected void fly();
	////////////////////////////////////////////////////////////////////////////
	@Override
	public String toString() {
		return "feathers " +
				(feathers == -1 ? "are not counted yet" : "= " + feathers) +
				"\tlays " + layEggs + " Egg" + (layEggs > 1 ? "s" : ""); 
	}
	////////////////////////////////////////////////////////////////////////////
}