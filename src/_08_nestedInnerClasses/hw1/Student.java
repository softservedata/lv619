package _08_nestedInnerClasses.hw1;

public final class Student extends Person {
	////////////////////////////////////////////////////////////////////////////
	private int course;
	////////////////////////////////////////////////////////////////////////////
	public Student(String firstName, String lastName, int age, int course) {
		super(new FullName(firstName, lastName), age);
		this.course = course;
	}
	////////////////////////////////////////////////////////////////////////////
	public int getCourse() {return course;}
	////////////////////////////////////////////////////////////////////////////
	@Override
	public String info() {return super.info() + "\tcourse: " + getCourse();}
	////////////////////////////////////////////////////////////////////////////
	@Override
	public String activity() {return "I study at university";}
	////////////////////////////////////////////////////////////////////////////
}