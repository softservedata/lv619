package _06_oop_1.hw2;

final class Developer extends Employee {
	////////////////////////////////////////////////////////////////////////////
	private String position;
	////////////////////////////////////////////////////////////////////////////
	Developer(String name, int age, double salary, String position) {
		super(name, age, salary);
		this.position = position;
	}
	////////////////////////////////////////////////////////////////////////////
		@Override
		public String toString() {
			return (super.toString()).replace("Employee", "Developer") +
					", position: " + position; 
		}
	////////////////////////////////////////////////////////////////////////////
}