package _08_nestedInnerClasses.hw1;

public abstract class Person {
	////////////////////////////////////////////////////////////////////////////
	static class FullName{
		////////////////////////////////////////////////////////////////////////
		private String firstName;
		private String lastName;
		////////////////////////////////////////////////////////////////////////
		FullName(String firstName, String lastName){
			this.firstName = firstName;
			this.lastName = lastName;
		}
		////////////////////////////////////////////////////////////////////////
		private String getFirstName() {return firstName;}
		private String getLastName() {return lastName;}
		////////////////////////////////////////////////////////////////////////
		public String toString() {
			return "first name: " + getFirstName() +
					"\tlast name: " + getLastName();
		}
		////////////////////////////////////////////////////////////////////////
	}
	////////////////////////////////////////////////////////////////////////////
	private FullName fullName;
	private int age;
	////////////////////////////////////////////////////////////////////////////
	protected Person(FullName fullName, int age){
		this.fullName = fullName;
		this.age = age;
	}
	////////////////////////////////////////////////////////////////////////////
	protected FullName getFullName() {return fullName;}
	protected int getAge() {return age;}
	////////////////////////////////////////////////////////////////////////////
	protected String info() {return getFullName() + "\tage: " + getAge();}
	////////////////////////////////////////////////////////////////////////////
	protected abstract String activity();
	////////////////////////////////////////////////////////////////////////////
}