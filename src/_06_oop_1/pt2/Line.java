package _06_oop_1.pt2;

class Line {
	////////////////////////////////////////////////////////////////////////////
	private Point start;
	private Point end;
	////////////////////////////////////////////////////////////////////////////
	Line(Point start, Point end) {
		this.start = start;
		this.end = end;
	}
	////////////////////////////////////////////////////////////////////////////
	void print() {System.out.println(this);}
	//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
	@Override
	public String toString() {
		return "start" + start + " end" + end;
	}
	////////////////////////////////////////////////////////////////////////////
}